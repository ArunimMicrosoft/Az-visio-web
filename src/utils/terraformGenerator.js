/**
 * Generate networking configuration
 */
const generateNetworkingConfig = (items) => {
  return `# Network Security Group
resource "azurerm_network_security_group" "main" {
  name                = "nsg-main"
  location            = azurerm_resource_group.main.location
  resource_group_name = azurerm_resource_group.main.name
  
  security_rule {
    name                       = "allow-ssh"
    priority                   = 100
    direction                  = "Inbound"
    access                     = "Allow"
    protocol                   = "Tcp"
    source_port_range          = "*"
    destination_port_range     = "22"
    source_address_prefix      = "*"
    destination_address_prefix = "*"
  }
}`;
};

/**
 * Generate output values
 */
const generateOutput = (item, index) => {
  const resourceName = `${item.serviceType}_${index}`;
  
  const outputs = {
    vm: `output "${resourceName}_ip" {
  value = azurerm_linux_virtual_machine.${resourceName}.private_ip_address
}`,
    storage: `output "${resourceName}_connection_string" {
  value     = azurerm_storage_account.${resourceName}.primary_connection_string
  sensitive = true
}`,
    sqldb: `output "${resourceName}_connection_string" {
  value     = azurerm_mssql_server.${resourceName}_server.fully_qualified_domain_name
  sensitive = true
}`,
    keyvault: `output "${resourceName}_uri" {
  value = azurerm_key_vault.${resourceName}.vault_uri
}`
  };
  
  return outputs[item.serviceType] || null;
};

/**
 * Download Terraform configuration as .tf file
 */
export const downloadTerraform = (items, connections) => {
  const config = generateTerraform(items, connections);
  const blob = new Blob([config], { type: 'text/plain' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = `azure-architecture-${Date.now()}.tf`;
  link.click();
  URL.revokeObjectURL(url);
};
