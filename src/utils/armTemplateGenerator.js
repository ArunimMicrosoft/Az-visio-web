/**
 * Generate ARM output for a resource
 */
const generateARMOutput = (item, index) => {
  const serviceName = item.name.toLowerCase().replace(/\s+/g, '-');
  const resourceName = `${serviceName}-${index}`;
  
  const outputs = {
    vm: {
      "type": "string",
      "value": `[reference(resourceId('Microsoft.Network/networkInterfaces', '${resourceName}-nic')).ipConfigurations[0].properties.privateIPAddress]`
    },
    storage: {
      "type": "string",
      "value": `[reference(resourceId('Microsoft.Storage/storageAccounts', '${resourceName.replace(/-/g, '')}sa')).primaryEndpoints.blob]`
    },
    sqldb: {
      "type": "string",
      "value": `[reference(resourceId('Microsoft.Sql/servers', '${resourceName}-server')).fullyQualifiedDomainName]`
    }
  };
  
  return outputs[item.serviceType] || null;
};

/**
 * Download ARM Template as JSON file
 */
export const downloadARMTemplate = (items, connections) => {
  const template = generateARMTemplate(items, connections);
  const json = JSON.stringify(template, null, 2);
  const blob = new Blob([json], { type: 'application/json' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = `azure-architecture-arm-${Date.now()}.json`;
  link.click();
  URL.revokeObjectURL(url);
};
