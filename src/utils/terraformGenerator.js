// Terraform Configuration Generator for Azure Architecture
// Converts visual architecture design to Terraform .tf files

/**
 * Generate Terraform configuration from architecture items and connections
 * @param {Array} items - Array of Azure service items on canvas
 * @param {Array} connections - Array of connections between items
 * @returns {string} - Complete Terraform configuration
 */
export const generateTerraform = (items, connections) => {
  const config = [];
  
  // Header
  config.push(`# Azure Architecture - Terraform Configuration`);
  config.push(`# Generated by Azure Architecture Designer`);
  config.push(`# Date: ${new Date().toISOString()}\n`);
  
  // Provider configuration
  config.push(`terraform {`);
  config.push(`  required_providers {`);
  config.push(`    azurerm = {`);
  config.push(`      source  = "hashicorp/azurerm"`);
  config.push(`      version = "~> 3.0"`);
  config.push(`    }`);
  config.push(`  }`);
  config.push(`}\n`);
  
  config.push(`provider "azurerm" {`);
  config.push(`  features {}`);
  config.push(`}\n`);
  
  // Resource Group
  config.push(`resource "azurerm_resource_group" "main" {`);
  config.push(`  name     = "rg-azure-architecture"`);
  config.push(`  location = "East US"`);
  config.push(`}\n`);
  
  // Generate resources for each item
  items.forEach((item, index) => {
    const resourceConfig = generateResourceConfig(item, index);
    if (resourceConfig) {
      config.push(resourceConfig);
      config.push('');
    }
  });
  
  // Add networking resources if VNets exist
  const vnets = items.filter(item => item.serviceType === 'vnet');
  if (vnets.length > 0) {
    config.push(generateNetworkingConfig(items));
  }
  
  // Output values
  config.push(`# Outputs`);
  items.forEach((item, index) => {
    const output = generateOutput(item, index);
    if (output) {
      config.push(output);
    }
  });
  
  return config.join('\n');
};

