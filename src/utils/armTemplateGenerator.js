// ARM Template Generator for Azure Architecture
// Converts visual architecture design to Azure Resource Manager templates

/**
 * Generate ARM Template from architecture items and connections
 * @param {Array} items - Array of Azure service items on canvas
 * @param {Array} connections - Array of connections between items
 * @param {Object} options - Optional: compliance, branding, author, version, timestamp
 * @returns {Object} - Complete ARM template JSON
 */
export const generateARMTemplate = (items, connections, options = {}) => {
  const {
    complianceSummary = {},
    branding = "Arunim's IT Caffe",
    author = 'Arunim Pandey',
    version = '2.1.0',
    timestamp = new Date().toISOString(),
    complianceBadge = '🟢',
  } = options;

  const template = {
    "$schema": "https://schema.management.azure.com/schemas/2019-04-01/deploymentTemplate.json#",
    "contentVersion": "1.0.0.0",
    "metadata": {
      "description": "Azure Architecture - Generated by Azure Architecture Designer",
      "branding": branding,
      "complianceBadge": complianceBadge,
      "complianceSummary": complianceSummary,
      "version": version,
      "author": author,
      "timestamp": timestamp,
      "generatedDate": timestamp
    },
    "parameters": {
      "location": {
        "type": "string",
        "defaultValue": "[resourceGroup().location]",
        "metadata": {
          "description": "Location for all resources"
        }
      },
      "environment": {
        "type": "string",
        "defaultValue": "production",
        "allowedValues": ["development", "staging", "production"],
        "metadata": {
          "description": "Environment name"
        }
      }
    },
    "variables": {
      "resourcePrefix": "[concat('azure-arch-', parameters('environment'))]"
    },
    "resources": [],
    "outputs": {}
  };
  
  // Generate resources for each item
  items.forEach((item, index) => {
    const resources = generateARMResource(item, index);
    if (resources) {
      if (Array.isArray(resources)) {
        template.resources.push(...resources);
      } else {
        template.resources.push(resources);
      }
    }
    
    // Generate outputs
    const output = generateARMOutput(item, index);
    if (output) {
      template.outputs[`${item.serviceType}_${index}`] = output;
    }
  });
  
  return template;
};

